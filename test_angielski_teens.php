		//Set the fade in effect for the next image, the show class has higher z-index
		next.css({opacity: 0.0})
		.addClass('show')
		.animate({opacity: 1.0}, 1000);
		//Hide the current image
		current.animate({opacity: 0.0}, 1000)
		.removeClass('show');
	};
	$(document).ready(function() {		
		//Load the slideshow
		theRotator();
	});
